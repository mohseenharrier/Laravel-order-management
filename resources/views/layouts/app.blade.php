<script type="text/javascript">
$(document).on('click', '.button-delete', function (e) {
    e.preventDefault();
    var id = $(this).data('id');
        swal({
            title: "Are you sure!",
            type: "error",
            confirmButtonClass: "btn-danger",
            confirmButtonText: "Yes!",
            showCancelButton: true,
        }).then(function(isConfirm){
            if(isConfirm){
                $.ajax({
                        type:'DELETE',
                        url:'{{url("/orders/delete")}}/' +id,
                        data:{
                            "_token": "{{ csrf_token() }}",
                        },
                        success:function(data) {
                            if(data.status_code){
                                swal({
                                    title: "Alert!",
                                    type: "success",
                                    text: data.message
                                })
                                $('#order_'+id).remove();
                            }else{
                                swal({
                                    title: "Alert!",
                                    type: "error",
                                    text: data.message
                                })
                            }
                           
                        }
                });
            }   
        });
});
</script>
